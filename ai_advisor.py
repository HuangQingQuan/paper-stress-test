# 哪些检查项支持AI追问
AI_SUPPORTED_IDS = set(PROMPT_TEMPLATES.keys())


def get_ai_questions(check_id: str, excerpt: str) -> list[str]:
    """
    调用硅基流动，返回针对本文的3条具体追问。
    只在规则层检测为「未找到」且支持AI追问时调用。
    """
    template = PROMPT_TEMPLATES.get(check_id)
    if not template:
        return []

    prompt = template.format(excerpt=excerpt[:3000])

    try:
        response = client.chat.completions.create(
            model=AI_MODEL,
            max_tokens=400,
            messages=[{"role": "user", "content": prompt}]
        )
        raw = response.choices[0].message.content.strip()
        lines = [l.strip().lstrip("·").strip()
                 for l in raw.splitlines() if l.strip()]
        return [l for l in lines if len(l) > 5][:3]
    except Exception as e:
        return [f"AI追问暂时不可用（{str(e)[:80]}）"]
